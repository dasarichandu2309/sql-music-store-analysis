ORDER BY TOTAL_REVENUE DESC
LIMIT 1;

-- WHO IS THE BEST CUSTOMER?
SELECT C.CUSTOMER_ID,
       C.FIRST_NAME,
       C.LAST_NAME,
       SUM(I.TOTAL) AS TOTAL_SPENT
FROM CUSTOMER C
JOIN INVOICE I
ON C.CUSTOMER_ID = I.CUSTOMER_ID
GROUP BY C.CUSTOMER_ID, C.FIRST_NAME, C.LAST_NAME
ORDER BY TOTAL_SPENT DESC
LIMIT 1;


-- ROCK MUSIC LISTENERS (EMAIL, NAME, GENRE)
SELECT DISTINCT C.EMAIL,
       C.FIRST_NAME,
       C.LAST_NAME,
       G.NAME AS GENRE
FROM CUSTOMER C
JOIN INVOICE I ON C.CUSTOMER_ID = I.CUSTOMER_ID
JOIN INVOICELINE IL ON I.INVOICE_ID = IL.INVOICE_ID
JOIN TRACK T ON IL.TRACK_ID = T.TRACK_ID
JOIN GENRE G ON T.GENRE_ID = G.GENRE_ID
WHERE G.NAME = 'ROCK'
ORDER BY C.EMAIL;

-- TOP 10 ROCK ARTISTS
SELECT AR.NAME AS ARTIST_NAME,
       COUNT(T.TRACK_ID) AS TRACK_COUNT
FROM ARTIST AR
JOIN ALBUM AL ON AR.ARTIST_ID = AL.ARTIST_ID
JOIN TRACK T ON AL.ALBUM_ID = T.ALBUM_ID
JOIN GENRE G ON T.GENRE_ID = G.GENRE_ID
WHERE G.NAME = 'ROCK'
GROUP BY AR.NAME
ORDER BY TRACK_COUNT DESC
LIMIT 10;

-- TRACKS LONGER THAN AVERAGE

SELECT NAME,
       MILLISECONDS
FROM TRACK
WHERE MILLISECONDS > (
    SELECT AVG(MILLISECONDS) FROM TRACK
)
ORDER BY MILLISECONDS DESC;


-- AMOUNT SPENT BY EACH CUSTOMER ON ARTISTS
SELECT C.FIRST_NAME,
       C.LAST_NAME,
       AR.NAME AS ARTIST_NAME,
       SUM(IL.UNIT_PRICE * IL.QUANTITY) AS TOTAL_SPENT
FROM CUSTOMER C
JOIN INVOICE I ON C.CUSTOMER_ID = I.CUSTOMER_ID
JOIN INVOICELINE IL ON I.INVOICE_ID = IL.INVOICE_ID
JOIN TRACK T ON IL.TRACK_ID = T.TRACK_ID
JOIN ALBUM AL ON T.ALBUM_ID = AL.ALBUM_ID
JOIN ARTIST AR ON AL.ARTIST_ID = AR.ARTIST_ID
GROUP BY C.FIRST_NAME, C.LAST_NAME, AR.NAME
ORDER BY TOTAL_SPENT DESC;

-- MOST POPULAR GENRE FOR EACH COUNTRY
SELECT I.BILLING_COUNTRY,
       G.NAME,
       COUNT(*) AS TOTAL
FROM INVOICE I,
     INVOICELINE IL,
     TRACK T,
     GENRE G
WHERE I.INVOICE_ID = IL.INVOICE_ID
AND IL.TRACK_ID = T.TRACK_ID
AND T.GENRE_ID = G.GENRE_ID
GROUP BY I.BILLING_COUNTRY, G.NAME
ORDER BY I.BILLING_COUNTRY, TOTAL DESC;



