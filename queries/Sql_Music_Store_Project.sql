-- 7. INVOICE
CREATE TABLE INVOICE (
    INVOICE_ID INT PRIMARY KEY,
    CUSTOMER_ID INT,
    INVOICE_DATE DATE,
    BILLING_ADDRESS VARCHAR(255),
    BILLING_CITY VARCHAR(100),
    BILLING_STATE VARCHAR(100),
    BILLING_COUNTRY VARCHAR(100),
