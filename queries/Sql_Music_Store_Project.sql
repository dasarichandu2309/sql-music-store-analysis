SELECT BILLING_COUNTRY,
       COUNT(*) AS TOTAL_INVOICES
FROM INVOICE
GROUP BY BILLING_COUNTRY
ORDER BY TOTAL_INVOICES DESC
LIMIT 1;


-- WHAT ARE THE TOP 3 VALUES OF TOTAL INVOICE?
SELECT TOTAL
FROM INVOICE
ORDER BY TOTAL DESC
LIMIT 3;

-- WHICH CITY HAS THE BEST CUSTOMERS?
SELECT BILLING_CITY,
       SUM(TOTAL) AS TOTAL_REVENUE
FROM INVOICE
